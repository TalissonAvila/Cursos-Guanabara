    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        panDelta.setVisible(true);
        
        int numeroA = Integer.parseInt(txtA.getValue().toString());
        int numeroB = Integer.parseInt(txtB.getValue().toString());
        int numeroC = Integer.parseInt(txtC.getValue().toString());
        
        float delta = (numeroB * numeroB) - 4 * numeroA * numeroC;
        
        lblDelta.setText(Float.toString(delta));
        
        if(delta >= 0){
            lblRaiz.setText("Existem Raizes Reais");
        } else{
            lblRaiz.setText("NÃ£o Existem Raizes Reais");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

